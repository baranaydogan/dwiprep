#            report.write('\n}');
#            report.close();
#            sys.exit('PartialFourier in ' + basenameInp + ' does not match others'); 
    
#    try:
#        aqAxes=dwiInfo['ImageOrientationPatientDICOM'];
#        if ((aqAxes[0]==0) & (aqAxes[1]==0) & (aqAxes[2]==0)) | ((aqAxes[3]==0) & (aqAxes[4]==0) & (aqAxes[5]==0)) :
#            raise;
#        report.write(',\n"ImageOrientationPatientDICOM": ' +str(aqAxes));
#    except:            
#        report.write(',\n"result": "FAILED, ImageOrientationPatientDICOM missing or wrong ' + (basenameInp + '.json') + '"');
#        report.write('\n}');
#        report.close();
