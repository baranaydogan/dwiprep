# Verify input
def run_verifyInput(dwi,basenameOut,reference_report):
    
    import os
    import sys
    import nibabel as nib
    import numpy as np
    import json
    
    refInfo=[];
    if reference_report:
        fid = open(reference_report);
        refInfo=json.load(fid);
        fid.close();    
    
    report      = open((basenameOut + '.json'),'w');
    report.write('{\n');
    report.write('"generated by": "dwiprep -> verifyInput -> run_verifyInput"');
    report.write(',\n"input": "' + dwi + '"');
    report.write(',\n"output": "' + basenameOut + '.json"');    
    
    sys.stdout.write('   Verifying input... ');
    
    if (dwi[-3:]=='.gz'):
        basename    = os.path.basename(dwi[0:-7]);
    else:
        basename    = os.path.basename(dwi[0:-4]);
         
    basenameInp = os.path.dirname(dwi) + '/' + basename;
    
    nii         = nib.load(dwi);
    ndim        = len(nii.shape);
    
    if (ndim==4):
        nv      = nii.shape[3];
    elif (ndim==3):
        nv      = 1;
    else:
        report.write(',\n"image dimension": "' + str(ndim) + '"');
        report.write((',\n"result": "FAILED, input dimension cannot be '+ str(ndim) +'"' ));
        report.write('\n}');
        report.close();
        sys.exit('input dimension cannot be ' + str(ndim) + ' :' + dwi);

    report.write(',\n"dcmmeta_shape": [' + str(nii.shape[0]) + ', ' + str(nii.shape[1]) + ', ' + str(nii.shape[2]) + ']');
    report.write(',\n"number of volumes": ' +str(nv));
        
    
    bval        = np.atleast_1d(np.loadtxt((basenameInp + '.bval')));
    
    if (bval.shape[0]!=nv):
        report.write(',\n"bval count": "' + str(bval.shape[0]) + '"');
        report.write(',\n"result": "FAILED, bval count does not match number of volumes"');
        report.write('\n}');
        report.close();
        sys.exit('bval count does not match number of volumes');
    
    bvec        = np.atleast_2d(np.loadtxt((basenameInp + '.bvec')));
    
    if (bvec.shape[0]==1):
        bvec=bvec.transpose();
    
    if (bvec.shape[0]!=3):
        report.write(',\n"bvec row count": "'+str(bvec.shape[0])+'"');
        report.write(',\n"result": "FAILED, bvec row count is not 3"');
        report.write('\n}');
        report.close();
        sys.exit('bvec row count is not 3');
        
    if (bvec.shape[1]!=nv):
        report.write(',\n"bvec column count": "'+str(bvec.shape[1])+'"');
        report.write(',\n"result": "FAILED, bvec column count does not match number of volumes"');
        report.write('\n}');
        report.close();
        sys.exit('bvec column count does not match number of volumes');

    fid = open((basenameInp + '.json'));
    dwiInfo=json.load(fid);
    fid.close();
    
    
    # Verify PartialFourier which is needed for Gibbs artefact removal
    try:
        PartialFourier = float(dwiInfo['PartialFourier']);
        report.write(',\n"PartialFourier": ' +str(PartialFourier));
    except:            
        report.write(',\n"result": "FAILED, PartialFourier missing or wrong ' + (basenameInp + '.json') + '"');
        report.write('\n}');
        report.close();
        sys.exit('missing PartialFourier in ' + (basenameInp + '.json'))

#    THIS MIGHT NOT BE NEEDED        
#    if refInfo : 
